export function getSharedDailyWritingKernel() {
  return `
## 共享写作内核
- 说人话，不写公文腔，不堆模型黑话
- 先给结果，再补原因；先给画面，再补概念
- 能用具体场景就不用抽象判断
- 多写反差、痛点和结果，少写空泛总结
- 像朋友聊天，不像系统播报
- 长短句交替，别整段一个节奏
- 少重复同一种开头和句式
- 展示，而不是解释；让读者脑子里有画面
- 素材普通也要写成成品，不准输出过程说明、拒答或索要素材
`.trim();
}

export function getSharedOpportunityWritingKernel() {
  return `
${getSharedDailyWritingKernel()}

## 商机版落地要求
- 像日报，不像表单
- 不是在填系统字段，而是在给读者讲今天最值得试的小机会
- 先替读者做判断：今天最值得先试哪一单，不要把所有机会写得一样重
- 把热闹翻译成小白能做的小机会
- 先写买家今天为什么会心动，再写工具和技术变化
- 先讲能拿到什么结果，再讲怎么卖
- 少一点卖货腔，多一点像懂行朋友帮人省判断时间
- 可以借一点熟悉场景、小麻烦和轻微共情，但不要写成段子或表演腔
- 像朋友聊天，不像系统填表
- 标题要有钩子，优先写结果、反差或熟悉场景，不要平铺工具名
- 先说结论要短，可以单独截图转发
- 开头第一句要像朋友圈转发导语，让人一眼想点开
- 句子要短，有记忆点，最好读完能复述出来
- 让小白看完后有“我也能试一下”的冲动，而不是只觉得你分析得对
`.trim();
}
